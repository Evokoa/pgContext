#[pg_test]
fn collection_alias_routes_search_and_can_retarget() {
    create_alias_search_collection("m12_alias_docs_a", "[1,0]", "1");
    create_alias_search_collection("m12_alias_docs_b", "[0,1]", "2");

    let created = alias_rows(
        "SELECT alias_name, collection_name
           FROM pgcontext.create_collection_alias('m12_alias_live', 'm12_alias_docs_a')",
    );
    assert_eq!(
        created,
        vec![("m12_alias_live".to_owned(), "m12_alias_docs_a".to_owned())]
    );
    assert_eq!(alias_search_source_keys("m12_alias_live", "[1,0]"), vec!["1"]);

    let retargeted = alias_rows(
        "SELECT alias_name, collection_name
           FROM pgcontext.create_collection_alias('m12_alias_live', 'm12_alias_docs_b')",
    );
    assert_eq!(
        retargeted,
        vec![("m12_alias_live".to_owned(), "m12_alias_docs_b".to_owned())]
    );
    assert_eq!(alias_search_source_keys("m12_alias_live", "[0,1]"), vec!["2"]);
}

#[pg_test]
fn collection_aliases_lists_visible_aliases_and_drop_removes_route() {
    create_alias_search_collection("m12_alias_list_docs", "[1,0]", "1");
    Spi::run(
        "SELECT pgcontext.create_collection_alias('m12_alias_list_live', 'm12_alias_list_docs')",
    )
    .expect("alias should be created");

    let aliases = alias_rows(
        "SELECT alias_name, collection_name
           FROM pgcontext.collection_aliases()
          WHERE alias_name = 'm12_alias_list_live'",
    );
    assert_eq!(
        aliases,
        vec![(
            "m12_alias_list_live".to_owned(),
            "m12_alias_list_docs".to_owned(),
        )]
    );
    let dropped = Spi::get_one::<bool>(
        "SELECT pgcontext.drop_collection_alias('m12_alias_list_live')",
    )
    .expect("drop alias query should succeed")
    .expect("drop alias should not return null");
    assert!(dropped);

    let aliases = alias_rows(
        "SELECT alias_name, collection_name
           FROM pgcontext.collection_aliases()
          WHERE alias_name = 'm12_alias_list_live'",
    );
    assert!(aliases.is_empty());
}

