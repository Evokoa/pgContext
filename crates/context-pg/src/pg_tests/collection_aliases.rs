fn create_alias_search_collection(collection_name: &str, vector: &str, source_key: &str) {
    Spi::run(&format!(
        "CREATE TABLE public.{collection_name} (
             id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
             embedding vector
         )"
    ))
    .expect("alias fixture source table should be created");
    Spi::run(&format!(
        "INSERT INTO public.{collection_name} (id, embedding)
         VALUES ({source_key}, '{vector}'::vector)"
    ))
    .expect("alias fixture source row should be inserted");
    Spi::run(&format!(
        "SELECT pgcontext.create_collection('{collection_name}', 'public.{collection_name}')"
    ))
    .expect("alias fixture collection should be created");
    Spi::run(&format!(
        "SELECT pgcontext.register_vector(
            '{collection_name}',
            'embedding',
            'embedding',
            2,
            'l2'
        )"
    ))
    .expect("alias fixture vector should be registered");
    Spi::run(&format!(
        "SELECT pgcontext.upsert_points('{collection_name}', ARRAY['{source_key}'])"
    ))
    .expect("alias fixture point should be registered");
}

fn alias_search_source_keys(collection_name: &str, vector: &str) -> Vec<String> {
    Spi::connect(|client| {
        let rows = client.select(
            &format!(
                "SELECT source_key
                   FROM pgcontext.search('{collection_name}', '{vector}'::vector, 1)"
            ),
            None,
            &[],
        )?;
        let mut output = Vec::new();
        for row in rows {
            output.push(
                row.get::<String>(1)?
                    .expect("source_key should not be null"),
            );
        }
        Ok::<_, spi::Error>(output)
    })
    .expect("alias search query should succeed")
}

fn alias_rows(sql: &str) -> Vec<(String, String)> {
    Spi::connect(|client| {
        let rows = client.select(sql, None, &[])?;
        let mut output = Vec::new();
        for row in rows {
            output.push((
                row.get::<String>(1)?.expect("alias_name should not be null"),
                row.get::<String>(2)?
                    .expect("collection_name should not be null"),
            ));
        }
        Ok::<_, spi::Error>(output)
    })
    .expect("alias rows query should succeed")
}
