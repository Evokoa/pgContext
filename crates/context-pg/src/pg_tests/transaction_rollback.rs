#[pg_test]
fn rolled_back_create_collection_leaves_no_catalog_row() {
    Spi::run(
        r#"
        DO $$
        BEGIN
            BEGIN
                PERFORM pgcontext.create_collection('m2_tx_create_rollback');
                RAISE EXCEPTION 'force catalog rollback';
            EXCEPTION WHEN others THEN
                NULL;
            END;
        END $$;
        "#,
    )
    .expect("rollback block should complete");

    assert_eq!(rollback_collection_count("m2_tx_create_rollback"), 0);
}

#[pg_test]
fn rolled_back_register_vector_leaves_no_vector_row() {
    rollback_create_source_table("m2_tx_vector_rollback", "embedding vector");
    Spi::run(
        "SELECT pgcontext.create_collection('m2_tx_vector_rollback', 'public.m2_tx_vector_rollback')",
    )
    .expect("table-backed collection should be created");

    Spi::run(
        r#"
        DO $$
        BEGIN
            BEGIN
                PERFORM pgcontext.register_vector(
                    'm2_tx_vector_rollback',
                    'embedding',
                    'embedding',
                    3,
                    'l2'
                );
                RAISE EXCEPTION 'force vector rollback';
            EXCEPTION WHEN others THEN
                NULL;
            END;
        END $$;
        "#,
    )
    .expect("rollback block should complete");

    assert_eq!(rollback_vector_count("m2_tx_vector_rollback"), 0);
}

