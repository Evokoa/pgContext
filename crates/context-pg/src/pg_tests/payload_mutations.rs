#[pg_test]
fn set_payload_rejects_unknown_points_without_partial_updates() {
    create_payload_mutation_collection("m12_payload_missing_point");

    let result = std::panic::catch_unwind(|| {
        Spi::run(
            "SELECT pgcontext.set_payload(
                'm12_payload_missing_point',
                ARRAY['1', 'missing'],
                '{\"topic\":\"changed\"}'::jsonb
            )",
        )
        .expect("missing source key should fail");
    });
    assert!(result.is_err(), "missing point should panic");
    assert_eq!(
        payload_row("m12_payload_missing_point", 1).metadata,
        serde_json::json!({"topic":"rust"})
    );
}

#[pg_test]
#[should_panic(expected = "payload field is not nullable for collection m12_payload_not_null: status")]
