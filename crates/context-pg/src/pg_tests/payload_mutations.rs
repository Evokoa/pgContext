#[pg_test]
fn set_payload_updates_jsonb_paths_and_typed_columns() {
    create_payload_mutation_collection("m12_payload_set");

    let rows = payload_mutation_rows(
        "SELECT source_key, updated
           FROM pgcontext.set_payload(
                'm12_payload_set',
                ARRAY['1'],
                '{\"topic\":\"postgres\",\"status\":\"closed\",\"priority\":5,\"archived\":true}'::jsonb
           )",
    );
    assert_eq!(rows, vec![("1".to_owned(), true)]);

    let row = payload_row("m12_payload_set", 1);
    assert_eq!(row.status.as_deref(), Some("closed"));
    assert_eq!(row.priority, Some(5));
    assert_eq!(row.archived, Some(true));
    assert_eq!(row.metadata, serde_json::json!({"topic":"postgres"}));
}

