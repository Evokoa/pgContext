#[pg_test]
fn clear_payload_removes_registered_nullable_payloads() {
    create_payload_mutation_collection("m12_payload_clear");
    Spi::run(
        "SELECT pgcontext.delete_payload('m12_payload_clear', ARRAY['1'], ARRAY['status'])",
    )
    .expect("status should be nullable before clear");

    let rows = payload_mutation_rows(
        "SELECT source_key, updated
           FROM pgcontext.clear_payload('m12_payload_clear', ARRAY['1'])",
    );
    assert_eq!(rows, vec![("1".to_owned(), true)]);

    let row = payload_row("m12_payload_clear", 1);
    assert_eq!(row.status, None);
    assert_eq!(row.priority, None);
    assert_eq!(row.archived, None);
    assert_eq!(row.metadata, serde_json::json!({}));
}

