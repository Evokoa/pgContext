#[derive(Debug, PartialEq)]
struct PayloadRow {
    status: Option<String>,
    priority: Option<i32>,
    archived: Option<bool>,
    metadata: serde_json::Value,
}

fn create_payload_mutation_collection(collection_name: &str) {
    Spi::run(&format!(
        "CREATE TABLE public.{collection_name} (
             id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
             embedding vector,
             status text,
             priority integer,
             archived boolean,
             metadata jsonb NOT NULL DEFAULT '{{}}'::jsonb
         )"
    ))
    .expect("payload fixture table should be created");
    Spi::run(&format!(
        "INSERT INTO public.{collection_name}
            (id, embedding, status, priority, archived, metadata)
         VALUES
            (1, '[1,0]'::vector, 'open', 1, false, '{{\"topic\":\"rust\"}}'::jsonb)"
    ))
    .expect("payload fixture row should be inserted");
    Spi::run(&format!(
        "SELECT pgcontext.create_collection('{collection_name}', 'public.{collection_name}')"
    ))
    .expect("payload fixture collection should be created");
    Spi::run(&format!(
        "SELECT pgcontext.register_vector('{collection_name}', 'embedding', 'embedding', 2, 'l2')"
    ))
    .expect("payload fixture vector should be registered");
    Spi::run(&format!(
        "SELECT pgcontext.upsert_points('{collection_name}', ARRAY['1'])"
    ))
    .expect("payload fixture point should be registered");
    Spi::run(&format!(
        "SELECT pgcontext.register_filter_column('{collection_name}', 'status', 'status')"
    ))
    .expect("status payload should be registered");
    Spi::run(&format!(
        "SELECT pgcontext.register_filter_column('{collection_name}', 'priority', 'priority')"
    ))
    .expect("priority payload should be registered");
    Spi::run(&format!(
        "SELECT pgcontext.register_filter_column('{collection_name}', 'archived', 'archived')"
    ))
    .expect("archived payload should be registered");
    Spi::run(&format!(
        "SELECT pgcontext.register_jsonb_path('{collection_name}', 'topic', 'metadata', ARRAY['topic'])"
    ))
    .expect("topic payload should be registered");
}

fn payload_mutation_rows(sql: &str) -> Vec<(String, bool)> {
    Spi::connect(|client| {
        let rows = client.select(sql, None, &[])?;
        let mut output = Vec::new();
        for row in rows {
            output.push((
                row.get::<String>(1)?.expect("source_key should not be null"),
                row.get::<bool>(2)?.expect("updated should not be null"),
            ));
        }
        Ok::<_, spi::Error>(output)
    })
    .expect("payload mutation query should succeed")
}

fn payload_row(table_name: &str, id: i64) -> PayloadRow {
    Spi::connect(|client| {
        let rows = client.select(
            &format!(
                "SELECT status, priority, archived, metadata
                   FROM public.{table_name}
                  WHERE id = {id}"
            ),
            Some(1),
            &[],
        )?;
        let row = rows.first();
        Ok::<_, spi::Error>(PayloadRow {
            status: row.get::<String>(1)?,
            priority: row.get::<i32>(2)?,
            archived: row.get::<bool>(3)?,
            metadata: row
                .get::<pgrx::JsonB>(4)?
                .expect("metadata should not be null")
                .0,
        })
    })
    .expect("payload row query should succeed")
}
